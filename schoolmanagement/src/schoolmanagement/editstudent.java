            PreparedStatement ptstmt = conn.prepareStatement(sql);
            ptstmt.setString(1, name.getText());
            ptstmt.setString(2, fname.getText());
            ptstmt.setString(3, pnumb.getText());
            ptstmt.setString(4, fnumb.getText());
            ptstmt.setString(5, cla.getText());
            ptstmt.setString(6, rn.getText());
            ptstmt.setString(7, address.getText());
            ptstmt.setString(8, id.getText());

            ptstmt.executeUpdate();

            JOptionPane.showMessageDialog(null, "Record has been updated successfully!");
