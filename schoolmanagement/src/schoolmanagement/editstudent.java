            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = java.sql.DriverManager.getConnection("jdbc:mysql://localhost/sms?zeroDateTimeBehavior=CONVERT_TO_NULL", "root", "mysql123@");
            String sql = "update  student SET  name=?,fathername=?,phn=?,fphn=?,class=?,rollno=?,address=? where id=? ";
            //String sql = "update student SET `fathername`='" + fname + "',`name`='" + name + "',`class`='" + cl
